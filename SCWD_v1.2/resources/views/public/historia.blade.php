                        El club s’anà afermant social i esportivament, i l’any 1990, amb l’adquisició d’un local al centre de Terrassa i la creació de cinc nous equips masculins i dos de femenins, es consolidà la primera etapa d’aquell incipient projecte. Durant aquest temps el Club començà a aportar diversos jugadors a les seleccions nacionals, tant en la modalitat de hockey herba com en la de sala, en categories masculina i femenina.</p>
