                        L’any 2002, es decidí donar un nou impuls a la secció de hockey, i una nova junta accedí a la direcció del Línia 22, sota la batuta de Joaquín de Miguel. Els següents quatre anys es caracteritzaren per una forta activitat social i esportiva, amb la celebració de memorables esdeveniments com les Diades de final de temporada de hockey al club. També s’incidí en la captació de jugadors i es posà especial cura en les accions dirigides als més petits.
                        La darrera acció d’aquesta junta fou la signatura d’un conveni de col·laboració a tres bandes entre el CTLF, el Línia 22 i el Club Natació Terrassa (CNT). L’acord contemplava el Línia 22 com a secció de hockey de l’entitat terrassenca, permetent que els jugadors seguissin jugant amb la seva condició de socis de Les Fonts.</p>
