                        El 2011 el Pep Olivé es manté com a Director Tècnic de l’escola, incorporant-se també el Jordi Arnau (Medalla de Plata a Atlanta) com a entrenador de l’equip de Sènior Masculí.
                        A la Junta Directiva de Hoquei s’incorporà a Xavier Campos com a Coordinador d’activitats conjuntament amb Mari Menéndez. La resta d’integrants continua sent, el Nico Alonso com a responsable econòmic, la Mariela Freytes com a responsable social, el Xavier Masip com a coordinador d’organització i el Raúl Galy com a President de l’escola i la secció d’hoquei.
