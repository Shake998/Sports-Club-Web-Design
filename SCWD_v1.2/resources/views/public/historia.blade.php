                        Fou en aquest mateix any 92 quan es produí un relleu al capdavant del club. Amb la nova junta, presidida per Mireia Olivé, s’inicià també l’aposta més ambiciosa d’aquest projecte, la creació de l’escola de hockey del club, amb un capital humà inicial d’uns 25 nens i nenes d’entre 4 i 12 anys.</p>
